from google import genai
from google.genai import types
from config import config
import re


api_key = config.gemini_api_key.get_secret_value()
client = genai.Client(api_key=api_key)


SYSTEM_INSTRUCTION = """
Ты — ведущий ИИ-помощник и эксперт по проверке заданий ЕГЭ по информатике на Python.
Твоя цель — провести краткий, профессиональный аудит кода ученика.

Действуй строго по следующему алгоритму:
1. Вердикт: Напиши "Логика: Верно" или "Логика: Содержит ошибки". Кратко поясни суть проблемы (в 1-2 предложениях).
2. Анализ ошибок: Если есть баги, лаконично укажи на строки и объясни, как исправить. Если ошибок нет, пропусти этот шаг.
3. Эталонное решение: Приведи оптимальный, лаконичный код в блоке кода (```python ... ```). Код должен быть эффективным.
4. Совет: Дай 1 короткий лайфхак по синтаксису Python или фишке для ЕГЭ, которая упростит этот код.

ПРАВИЛА ОФОРМЛЕНИЯ И ОГРАНИЧЕНИЯ:
- Максимальная длина ответа — 2400 символов. Пиши без "воды", приветствий и лишних вступлений. Текст должен быть плотным и по делу.
- Для оформления используй стандартный Markdown: *жирный текст* для ключевых моментов, `одиночные кавычки` для переменных/функций в тексте, и тройные кавычки с указанием языка для блоков кода.
- Внутри блока кода НЕ пиши длинных комментариев, экономь место.
- Важно: избегай использования сырых знаков < и > вне блоков кода, либо пиши их словами, если это не код.
"""


async def analyze_code_with_gemini(task_number, task_condition, user_code):
    try:
        user_prompt = (
            f"Задание ЕГЭ №{task_number}.\n"
            f"Условие задачи:\n{task_condition}\n\n"
            f"Код ученика для проверки:\n{user_code}"
        )

        response = client.models.generate_content(
            model='gemini-3.1-flash-lite',
            contents=user_prompt,
            config=types.GenerateContentConfig(
                system_instruction=SYSTEM_INSTRUCTION,
                temperature=0.3,
            )
        )

        return response.text

    except Exception as e:
        return f"Ошибка при обращении к ИИ-сервису: {str(e)}\nПожалуйста, попробуйте отправить код еще раз."